# Aula 02: Variáveis

# Variável do tipo TEXTO (string)
nome = "Helena"
endereco = "Artin Agazarian"
profissao = "Atendente"


# Variável do tipo NÚMERO (int)
idade = 28
cpf = 43420649878
whatsApp = 16993243211
ano_nascimento = 1995
cep: 13563524
data_nascimento = "25/02/1995"
_id = 18001


# Variável do tipo NÚMERO QUEBRADO (float)
altura = 1.63
temperatura = 24.0
preco = 49.99


# Variável do tipo BINÁRIO (boolean)
curso_concluido = False
usuario_assinante = True


# Mostrar variáveis na tela
print(endereco)
print(nome)
print(altura)
print(curso_concluido)


# Juntar texto com variável
print("O cliente se chama "+nome)
print("Ele mora em",endereco)
print("Ele mora em "+endereco)

# Juntar texto com variável e mais texto
print("Ela tem",altura,"de altura")
print("Ela tem",idade,"anos")

#FString (atalho de variáveis no texto)
print(f"O cliente se chama {nome}, ela tem {idade} anos, ela mora {endereco}, e seu whatsApp é {whatsApp}")




































